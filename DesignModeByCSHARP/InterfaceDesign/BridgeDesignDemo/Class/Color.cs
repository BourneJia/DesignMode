//颜色为桥
public class Color
{
    public string color;
}

public class Red : Color 
{
    public Red()
    {
        this.color = "red";
    }
}

public class Green : Color
{
    public Green()
    {
        this.color = "green";
    }
}

public class Blue : Color
{
    public Blue()
    {
        this.color = "blue";
    }
}

public class Yellow : Color
{
    public Yellow()
    {
        this.color = "yellow";
    }
}